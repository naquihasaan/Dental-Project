    			<p>An infected tooth pulp will be removed during a root canal treatment, and the root canal will be filled and sealed to prevent the spread of further infection. The removal of the infected parts of the tooth makes it possible for the damaged tooth to be saved from being extracted, so you can still enjoy the function of that particular tooth.</p>
