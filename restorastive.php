    			<p>Dental bridges are used to close the gap left by several missing teeth, and are considered as fixed partial dentures. A dental bridge is attached to the natural teeth on either side of the space left by tooth loss; the bridge is made up of a series of false teeth which will “bridge” the gap left by missing teeth. With the help of dental bridges, you can once again flash bright smiles – even after you have suffered from tooth loss.</p>

